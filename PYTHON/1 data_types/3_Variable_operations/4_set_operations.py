# To get all the values from both sets 
s1 = {14, 12, 37, 44}
s2 = {12, 34, 44, 32}
print(s1.union(s2))

# To get values which are in both sets 
s1 = {14, 12, 37, 44}
s2 = {12, 34, 44, 32}
print(s1.intersection(s2))
